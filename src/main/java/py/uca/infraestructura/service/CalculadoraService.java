package py.uca.infraestructura.service;

public class CalculadoraService {

	public Integer sumaInt(int a, int b) {

		return a + b;

	}

	public Integer restaInt(int a, int b) {

		return a - b;

	}

	public Integer multiplicaInt(int a, int b) {

		return a * b;

	}

	public Integer divideInt(int a, int b) {

		return a / b;

	}

}
